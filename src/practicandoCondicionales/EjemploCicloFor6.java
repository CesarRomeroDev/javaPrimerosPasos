package practicandoCondicionales;

public class EjemploCicloFor6 {
	public static void main(String[] argm) {
		
		for ( int contador = 0; contador <= 10 ; contador++) {
			System.out.println(contador);
		}		
	}
}
//CUAL ES LA DIFERENCIA DE USAR UN CICLO WHILE CON UN CICLO FOR?
//EN CICLO WHILE PODEMOS USAR LA VARIBLE QUE INCREMENTA FUERA DEL SCOUP.
//EN CICLO FOR SOLO SE PUEDE USAR LA VARIABLE DENTRO DEL SCOUP.